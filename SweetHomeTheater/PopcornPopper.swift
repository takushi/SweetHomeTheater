import Foundation


class PoppcornPopper {
  /**
  ポップコーン製造機の電源を入れます
  */
  func on() {
    print("ポップコーン製造機をスイッチオン")
  }
  
  /**
  ポップコーン製造機の電源を切ります
  */
  func off() {
    print("ポップコーン製造機をスイッチオフ")
  }
  
  /**
  ポップコーンを作ります
  */
  func pop() {
    print("ポップコーン製造機がポップコーンを作っています")
  }
}